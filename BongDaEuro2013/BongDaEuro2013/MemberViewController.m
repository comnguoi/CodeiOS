-(void) viewWillAppear:(BOOL)animated {
    
    [super viewWillAppear:animated];
    
    NSString *memberFile = [[NSBundle mainBundle] pathForResource:@"member" ofType:@"plist"];
    _member = [[NSMutableArray alloc] initWithContentsOfFile:memberFile];
    
    _memberToLoad = [[NSMutableArray alloc] initWithContentsOfFile:memberFile];
    
    
    [_memberToLoad removeAllObjects];

    
    for (int i = 0; i < 3;  i++) {
        [_memberToLoad addObject:[_member objectAtIndex:(self.tableIndex + 1) * (self.teamIndex + 1) * 3 + i - 3]];
    }
    
    [self.tableView reloadData];
}

